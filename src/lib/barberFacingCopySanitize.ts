/** تنقية نصوص لوحة الحلاق — مرآة لـ `api/_lib/barberFacingCopySanitize.ts` */

const BARBER_FACING_COPY_REPLACEMENTS: ReadonlyArray<readonly [string, string]> = [
  ['المناوب يرفع إشارة للأسطول', 'توصية تشغيلية من المناوب'],
  ['أبلغنا قيادة الأسطول بإشارة كساد محلي', 'سجّلنا ملاحظة تشغيلية لدعم ظهورك في حيّك'],
  ['لدعم محيط المدار في حيّك', 'لدعم ظهورك في حيّك'],
  ['المنصة تبني الطلب بالتوازي عبر محطات النبض', 'المنصة تواصل دعم ظهورك على الخريطة والبحث'],
  ['قيادة الأسطول', 'فريق تشغيل المنصة'],
  ['قائد الأسطول', 'فريق تشغيل المنصة'],
  ['للأسطول', 'لفريق التشغيل'],
  ['الأسطول', 'فريق التشغيل'],
  ['محيط المدار', 'ظهورك على الخريطة'],
  ['محطات النبض', 'قنوات الظهور'],
  ['كساد محلي', 'هدوء محلي'],
  ['كساد', 'هدوء'],
  ['غرفة قيادة', 'فريق التشغيل'],
  ['القناة السرية', 'توجيهات تشغيلية'],
];

export function sanitizeBarberFacingCopyAr(text: string): string {
  let out = String(text ?? '');
  if (!out.trim()) return out;
  for (const [from, to] of BARBER_FACING_COPY_REPLACEMENTS) {
    out = out.split(from).join(to);
  }
  return out;
}
