export function partnerAssistantHintForPath(pathname: string): string {
  const p = pathname.replace(/\/+$/, '') || '/';
  if (p.includes('/partners/why')) {
    return 'هنا «لماذا تنضم؟»: ركّز على فكرة القرار على الخريطة؛ إن وضحت لك، انتقل للتسجيل بثقة أكبر.';
  }
  if (p.includes('/partners/story')) {
    return 'صفحة القصة تشرح منطق المسار؛ بعدها راجع سياسة الاشتراك ثم أرسل الطلب.';
  }
  if (p.includes('/partners/support')) {
    return 'هنا «استوديو دعم الشركاء»: رابطك الخاص يفصل محادثتك عن غيرك لمدة ساعة — انسخ الرابط إن أردت العودة لاحقاً من نفس الجهاز.';
  }
  if (p.includes('/register/success')) {
    return 'تم إرسال طلبك؛ راقب بريدك أو واتساب الفريق للمتابعة — الاستجابة السريعة تبدأ بوضوح بياناتك.';
  }
  if (p.includes('/register') && !p.includes('/register/success')) {
    return 'في التسجيل: راجع الباقات وثبّت بياناتك بدقة — الطلب الواضح يُعامل بسرعة أكبر.';
  }
  if (p.includes('subscription-policy')) {
    return 'هنا تفاصيل الاشتراك؛ اقرأها بهدوء فهي أساس علاقة رصينة بينك وبين المنصّة.';
  }
  if (p.includes('privacy')) {
    return 'خصوصية الشركاء توضح كيف نتعامل مع بياناتك — الشفافية جزء من الثقة التسويقية.';
  }
  if (p.includes('login')) {
    return 'بعد التفعيل، لوحة الحلاق تكمّل ما بدأته هنا — حضورك يصبح قابلاً للإدارة.';
  }
  if (p.endsWith('/partners') || p.endsWith('/partners/')) {
    return 'ابدأ من «لماذا تنضم؟» أو «احجز بنرك»؛ كلاهما يقوّي قرارك قبل التسجيل.';
  }
  return 'تجوّل في الروابط أعلاه؛ كل صفحة تكمّل صورة المنصّة الاستثمارية أمام عملائك.';
}
