export function composePartnerFieldSalesKnowledgePack(): string {
  const faqBlock = PARTNER_LANDING_FAQ_AR.map((item) => `- **${item.q}** → ${item.a}`).join('\n');
  const objectionBlock = PARTNER_OBJECTION_HANDLERS_AR.map(
    (item) => `- **${item.trigger}:** ${item.response}`,
  ).join('\n');
  const compareBlock = PARTNER_SOCIAL_VS_PLATFORM_ROWS_AR.map(
    (row) => `- **${row.channel}:** ${row.intent} · ${row.cost}`,
  ).join('\n');

  return [
    '### نصوص التفاوض والتسويق B2B (مُنقّحة — v2026-06-10)',
    `- خطاف البرونزي: ${PARTNER_TIER_MARKETING_HOOKS_AR.bronze}`,
    `- خطاف الذهبي: ${PARTNER_TIER_MARKETING_HOOKS_AR.gold}`,
    `- خطاف الماسي: ${PARTNER_TIER_MARKETING_HOOKS_AR.diamond}`,
    '',
    '### مقارنة سريعة',
    compareBlock,
    '',
    '### معالجة الاعتراضات',
    objectionBlock,
    '',
    '### FAQ الشركاء (أجب من هنا)',
    faqBlock,
    '',
    '### FAQ التسجيل (قبل الإرسال)',
    ...PARTNER_REGISTER_FAQ_AR.map((item) => `- **${item.q}** → ${item.a}`),
  ].join('\n');
}
