function walk(dir, files = []) {
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (skipDirs.has(ent.name)) continue;
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) walk(p, files);
    else if (exts.has(path.extname(ent.name))) files.push(p);
  }
  return files;
}

const changed = [];
for (const file of walk(root)) {
  let content = fs.readFileSync(file, 'utf8');
  const orig = content;
  for (const [from, to] of arabicReplacements) content = content.split(from).join(to);
  for (const [from, to] of englishReplacements) content = content.split(from).join(to);
  if (content !== orig) {
    fs.writeFileSync(file, content, 'utf8');
    changed.push(path.relative(root, file));
  }
}

const logPath = path.join(root, 'scripts', 'terminology-refactor-result.json');
fs.writeFileSync(logPath, JSON.stringify({ changedCount: changed.length, changed }, null, 2), 'utf8');
